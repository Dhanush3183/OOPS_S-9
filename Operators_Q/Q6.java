package Operators_Q;
import java.util.*;
public class Q6 {
	public static void main(String[] args)
	{
		Scanner sc = new Scanner(System.in);
		
		System.out.print("Enter the value of a : ");
		int a = sc.nextInt();
		
		System.out.print("Enter the value of b : ");
		int b = sc.nextInt();
		
		a++;
		b--;
		
		System.out.println(a);
		System.out.println(b);
	}
}
